    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(jTextField1.getText().equals("") && jPasswordField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Error in Password Reset Try again", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }
       else if(jPasswordField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Enter your New Password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField1.requestFocus();
        }
        else if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Enter your Employee ID", "Error", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }
        else if(jPasswordField2.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Re-type your new password", "Error", JOptionPane.ERROR_MESSAGE);
            jPasswordField2.requestFocus();
        }
        else{
        
        try {
            conn = new ConnectDB().getConnection();
            String sql = "UPDATE  Registry SET password=? WHERE Emp_ID=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, jPasswordField1.getText());
            pst.setString(2, jTextField1.getText());
            
            int update = pst.executeUpdate();
            if(update != 1)
               JOptionPane.showMessageDialog(null, "Password not Reset");
            else
            JOptionPane.showMessageDialog(null, "Your Password has been reset");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

