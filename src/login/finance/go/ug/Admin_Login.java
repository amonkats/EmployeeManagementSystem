    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        Manage_Attendance attend = new Manage_Attendance();
        if(jTextField1.getText().equals("") && jPasswordField1.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Enter Username and Password", "Login Error",JOptionPane.ERROR_MESSAGE);
        
        else{
            try {
                conn = new ConnectDB().getConnection();
                String sql = "SELECT * FROM Admin WHERE Username=? AND Password=?";
                pst = conn.prepareStatement(sql);
                pst.setString(1, jTextField1.getText());
                pst.setString(2, jPasswordField1.getText());
                rs = pst.executeQuery();

                int count = 0;
                while(rs.next()){
                    count = count + 1;
                }
                if(count == 1){
                    dispose();
                    attend.setVisible(true);
//                    attend.pack();
//                    attend.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

                    //            }else if(count > 1)
                //                 JOptionPane.showMessageDialog(null, "Duplicate Username or Password");
            }else
            JOptionPane.showMessageDialog(null, "Invalid Username or Password", "Login Error", JOptionPane.ERROR_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

